public class relational_operators
{
    public static void main(String Args[])
    {
        int x = 4500;
        int y = 450;

        boolean b = x > y;
        boolean c = x < y;
        boolean d = x == y;
        boolean e = x >= y;
        boolean f = x <= y;
        boolean g = x != y;

        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
        System.out.println(f);
        System.out.println(g);
    }    
}
